package by.iba.gomel.debugoff;

/**
 * Class in 'debugoff' package
 */
public class DebugClass {
    /**
     * Returns 'WARNING' and string variable
     *
     * @param arg
     *            - a string variable
     * @return WARNING arg
     */
    public String debug(final String arg) {
        return "WARNING " + arg;
    }
}
