package by.iba.gomel.debug;

/**
 * Class in 'debug' package
 */
public class DebugClass {
    /**
     * Returns 'INFO' and string variable
     * 
     * @param arg
     *            - a string variable
     * @return INFO arg
     */
    public String debug(final String arg) {
        return "INFO " + arg;
    }
}
